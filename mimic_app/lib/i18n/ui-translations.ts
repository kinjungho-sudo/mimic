export function normalizeUiText(value: string): string {
  return value.replace(/\s+/g, ' ').trim();
}

export function translateUiText(value: string, locale: AppLocale): string {
  if (locale === 'ko' || !/[가-힣]/.test(value)) return value;

  const leading = value.match(/^\s*/)?.[0] ?? '';
  const trailing = value.match(/\s*$/)?.[0] ?? '';
  const normalized = normalizeUiText(value);
  const exact = ENGLISH_UI_TRANSLATIONS[normalized];
  if (exact) return `${leading}${exact}${trailing}`;

  for (const [pattern, replacement] of DYNAMIC_TRANSLATIONS) {
    const match = normalized.match(pattern);
    if (match) return `${leading}${replacement(...match.slice(1))}${trailing}`;
  }

  return value;
}
